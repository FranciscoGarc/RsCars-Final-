    private void buscarDatos() {
        MostrarDatosTabla mostrarDatosTabla = new MostrarDatosTabla();
        String textoBusqueda = txtSearch.getText();
        mostrarDatosTabla.buscarDatosEnTabla(tbDatosCl, textoBusqueda);
    }

    private void cargarDatosTabla() {
        MostrarDatosTabla mostrarDatosTabla = new MostrarDatosTabla();
        mostrarDatosTabla.mostrarDatosEnTabla(tbDatosCl);

    }

    private void mostrarReporte() {
        try {
            Map<String, Object> parametros = new HashMap<>();
            parametros.put("Nombre", txtSearch.getText());
            parametros.put("Ape", txtSearch.getText());
            parametros.put("User", txtSearch.getText());

            JasperReport report = (JasperReport) JRLoader.loadObjectFromFile("src/Reportes/ClientesPa.jasper");
            JasperPrint jprint = JasperFillManager.fillReport(report, parametros, conx.getConexion());

            JasperViewer view = new JasperViewer(jprint, false);
            view.setTitle("Reporte clientes");
            view.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
            view.setVisible(true);

        } catch (JRException ex) {
            System.out.println(ex.toString());
        }
    }

